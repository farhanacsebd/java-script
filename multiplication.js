function nameMultiplication(num1, num2) {
    nameAdd = num1 * num2;
    return nameAdd;
}


var result = nameMultiplication(56, 34);
console.log(result);




function nameDivision(first, second) {
    nameAdd = first + second;
    return nameAdd;
}
var result = nameDivision(100, 5);
console.log(result);